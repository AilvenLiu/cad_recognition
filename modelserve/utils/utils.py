def convertBase64(image):
        # 将NumPy数组转换为PIL图像
    if isinstance(image, np.ndarray):
        # 如果是NumPy数组，使用PIL的fromarray函数将其转换为PIL图像
        if len(image.shape) == 4:
            image = image.squeeze(0).transpose((1, 2,0))
            print(f"convert:{image.shape}")
        # 检查数组数据类型并转换为 uint8
        if image.dtype != np.uint8:
            # 确保值在0-255范围内
            image = np.clip(image, 0, 1)
            # 转换为 uint8 类型
            image = (image * 255).astype(np.uint8)
            
        image = Image.fromarray(image)
        # 创建一个字节流对象
    buffered = BytesIO()
        # 将PIL图像保存到字节流中，格式为'JPEG'或'PNG'
    image.save(buffered, format="JPEG")
        # 获取字节流的bytes
    byte_data = buffered.getvalue()
        # 对字节数据进行Base64编码
    base64_encoded = base64.b64encode(byte_data)
        # 将编码后的bytes转换为字符串
    base64_string = base64_encoded.decode('utf-8')
    return base64_string


def letterbox(img, new_shape=640, auto=False, scaleFill=False, scaleUp=True):
    """
    python的信封图片缩放
    :param img: 原图
    :param new_shape: 缩放后的图片
    :param auto: 是否为自动
    :param scaleFill: 填充
    :param scaleUp: 向上填充
    :return:
    """
    shape = img.shape[:2]  # current shape[height,width]
    if isinstance(new_shape, int):
        new_shape = (new_shape, new_shape)
    r = min(new_shape[0] / shape[0], new_shape[1] / shape[1])
    if not scaleUp:
        r = min(r, 1.0)  # 确保不超过1
    ration = r, r  # width,height 缩放比例
    new_unpad = int(round(shape[1] * r)), int(round(shape[0] * r))
    dw, dh = new_shape[1] - new_unpad[0], new_shape[0] - new_unpad[1]
    if auto:
        dw, dh = np.mod(dw, 64), np.mod(dh, 64)
    elif scaleFill:
        dw, dh = 0.0, 0.0
        new_unpad = (new_shape[1], new_shape[0])
        ration = new_shape[1] / shape[1], new_shape[0] / shape[0]
    # 均分处理
    dw /= 2
    dh /= 2
    if shape[::-1] != new_unpad:
        img = cv2.resize(img, new_unpad, interpolation=cv2.INTER_LINEAR)
    top, bottom = int(round(dh - 0.1)), int(round(dh + 0.1))
    left, right = int(round(dw - 0.1)), int(round(dw + 0.1))
    img = cv2.copyMakeBorder(img, top, bottom, left, right, cv2.BORDER_CONSTANT, value=(114, 114, 114))  # 添加边界
    return img

def clip_coords(boxes, img_shape):
    """
    图片的边界处理
    :param boxes: 检测框
    :param img_shape: 图片的尺寸
    :return:
    """
    boxes[:, 0].clip(0, img_shape[1])  # x1
    boxes[:, 1].clip(0, img_shape[0])  # y1
    boxes[:, 2].clip(0, img_shape[1])  # x2
    boxes[:, 3].clip(0, img_shape[0])  # x2


def scale_coords(img1_shape, coords, img0_shape, ratio_pad=None):
    """
    坐标还原
    :param img1_shape: 旧图像的尺寸
    :param coords: 坐标
    :param img0_shape:新图像的尺寸
    :param ratio_pad: 填充率
    :return:
    """
    if isinstance(img1_shape, int):
        img1_shape = (img1_shape, img1_shape)
    if isinstance(img0_shape, int):
        img0_shape = (img0_shape, img0_shape)
        
    if ratio_pad is None:  # 从img0_shape中计算
        gain = min(img1_shape[0] / img0_shape[0], img1_shape[1] / img0_shape[1])  # gain=old/new
        pad = (img1_shape[1] - img0_shape[1] * gain) / 2, (img1_shape[0] - img0_shape[0] * gain) / 2
    else:
        gain = ratio_pad[0][0]
        pad = ratio_pad[1]

    coords[:, [0, 2]] -= pad[0]  # x padding
    coords[:, [1, 3]] -= pad[1]  # y padding
    coords[:, :4] /= gain
    clip_coords(coords, img0_shape)
    return coords

